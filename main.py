# A regular expression rule

def t_int_main(t):
    r"int\s*\t*main"
    t.type = reserved_words.get(t.value, "int_main")  # Check for reserved words
    return t

def t_declaration(t):
    r'([a-zA-Z_{1}][a-zA-Z0-9_]+)\s*\t*(?=\()'
    t.type = reserved_words.get(t.value, "declaration")  # Check for reserved words
    return t

def t_CONST_CHAR(t):
    r"(\')(.*)(\')"
    return t

